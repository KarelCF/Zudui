package so.zudui.entity;

import java.util.ArrayList;
import java.util.List;

public class Activities {
	
	public static final int STATUS_UPCOMING = 180;
	public static final int STATUS_ONGOING = 181;
	public static final int STATUS_FINISHED = 182;
	
	
	private List<Activity> activities;
	
	public List<Activity> getActivities() {
		return activities;
	}
	
	public static class Activity {
		
		int id;
		String name;
		String shopname;
		String address;
		int starttime;
		String shoptel;
		int pre_number;
		int activity_type;
		String introduce;
		float longitude;
		float latitude;
		String create_user_uid;
		String join_user_id;
		String iconUrl;
		int status;
		List<String> participantsAvatar = new ArrayList<String>();
		
		public int getId() {
			return id;
		}
		public void setId(int id) {
			this.id = id;
		}
	
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		public String getShopname() {
			return shopname;
		}
		public void setShopname(String shopname) {
			this.shopname = shopname;
		}
		public String getAddress() {
			return address;
		}
		public void setAddress(String address) {
			this.address = address;
		}
		public int getStarttime() {
			return starttime;
		}
		public void setStarttime(int starttime) {
			this.starttime = starttime;
		}
		public String getShoptel() {
			return shoptel;
		}
		public void setShoptel(String shoptel) {
			this.shoptel = shoptel;
		}
		public int getPreNumber() {
			return pre_number;
		}
		public void setPreNumber(int pre_number) {
			this.pre_number = pre_number;
		}
		public int getActivityType() {
			return activity_type;
		}
		public void setActivityType(int activity_type) {
			this.activity_type = activity_type;
		}
		public String getIntroduce() {
			return introduce;
		}
		public void setIntroduce(String introduce) {
			this.introduce = introduce;
		}
		public float getLongitude() {
			return longitude;
		}
		public void setLongitude(float longitude) {
			this.longitude = longitude;
		}
		public float getLatitude() {
			return latitude;
		}
		public void setLatitude(float latitude) {
			this.latitude = latitude;
		}
		public String getCreateUserUid() {
			return create_user_uid;
		}
		public void setCreateUserUid(String create_user_uid) {
			this.create_user_uid = create_user_uid;
		}
		public String getJoinUserId() {
			return join_user_id;
		}
		public void setJoinUserId(String join_user_id) {
			this.join_user_id = join_user_id;
		}
		public void setIconUrl (String iconUrl) {
			this.iconUrl = iconUrl;
		}
		public String getIconUrl () {
			return iconUrl;
		}
		public int getStatus() {
			return status;
		}
		public void setStatus(int status) {
			this.status = status;
		}
		public void addToParticipantsAvatar(String url) {
			participantsAvatar.add(url);
		}
		public List<String> getParticipantsAvatar() {
			return participantsAvatar;
		}
		public void clearParticipantsAvatar() {
			participantsAvatar.clear();
		}
		
	}
	
}
